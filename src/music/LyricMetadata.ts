export interface LyricMetadata {
    /**
     * 专辑名：al
     */
    albumName?: string;
    /**
     * 歌手名: ar
     */
    artistName?: string;
    /**
     * 歌词作者:au
     */
    artistAuthor?: string;
    /**
     * 歌词文件作者: by
     */
    by?: string;
    /**
     * 补偿: offset
     */
    offset?: number;
    /**
     * 创建歌词文件的播放器或编辑器名称: re
     */
    re?: string;
    /**
     * 歌曲（歌词）标题: ti
     */
    title?: string;
    /**
     * 程序版本: ve
     */
    vesion?: string;
}
